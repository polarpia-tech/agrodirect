import 'package:cloud_firestore/cloud_firestore.dart';
import '../models/price_model.dart';

class PriceService {
  final FirebaseFirestore _db = FirebaseFirestore.instance;

  // Φέρνει τις τιμές από το Firestore σε πραγματικό χρόνο
  Stream<List<PriceModel>> getDailyPrices() {
    return _db.collection('daily_prices').snapshots().map((snapshot) {
      return snapshot.docs.map((doc) {
        return PriceModel.fromMap(doc.id, doc.data());
      }).toList();
    });
  }

