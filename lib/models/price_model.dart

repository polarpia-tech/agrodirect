class PriceModel {
  final String id;
  final String productName;
  final String category;
  final double minPrice;
  final double maxPrice;
  final double avgPrice;
  final String unit;
  final String marketSource;

  PriceModel({
    required this.id,
    required this.productName,
    required this.category,
    required this.minPrice,
    required this.maxPrice,
    required this.avgPrice,
    required this.unit,
    required this.marketSource,
  });

  // Μετατρέπει τα δεδομένα από τη βάση (Firebase) σε μορφή που καταλαβαίνει η εφαρμογή
  factory PriceModel.fromMap(String id, Map<String, dynamic> map) {
    return PriceModel(
      id: id,
      productName: map['productName'] ?? '',
      category: map['category'] ?? '',
      minPrice: (map['minPrice'] ?? 0.0).toDouble(),
      maxPrice: (map['maxPrice'] ?? 0.0).toDouble(),
      avgPrice: (map['avgPrice'] ?? 0.0).toDouble(),
      unit: map['unit'] ?? '€/κιλό',
      marketSource: map['marketSource'] ?? '',
    );
  }
}
